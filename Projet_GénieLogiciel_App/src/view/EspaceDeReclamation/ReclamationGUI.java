class RepondreJDialog extends JDialog {

    private JTextArea raisonArea;


    public RepondreJDialog(JFrame parent, Reclamation Reclamation) {
        super(parent, "Repondre au reclamation", true);
        setSize(300, 200);

        JLabel messageLabel = new JLabel("Veuillez repondre a la reclamation:");
        raisonArea = new JTextArea(5, 20);  
        JButton sendReasonButton = new JButton("Envoyer r√©ponse");

        // Use FlowLayout with vertical alignment
        setLayout(new FlowLayout(FlowLayout.CENTER, 10, 10));

        // Add components to the dialog
        add(messageLabel);
        add(raisonArea);
        add(sendReasonButton);
      

        // Add action listeners for the buttons
        sendReasonButton.addActionListener((ActionEvent e) -> {
            // Get the entered reason
            String reponse = raisonArea.getText();
            // Print the reason to the console (you can replace this with your actual logic)
            System.out.println("La r√©ponse : " + reponse);
            new EnvoyerEmail(Reclamation, reponse);
            // Close the dialog
            dispose();
        });

        
        setDefaultCloseOperation(JDialog.DISPOSE_ON_CLOSE);
        setLocationRelativeTo(parent);
    }
    
}

